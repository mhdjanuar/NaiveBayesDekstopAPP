    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // Ambil nama karyawan dari combo box
        String selectedName = (String) jComboBox1.getSelectedItem();
        if (selectedName == null || !karyawanMap.containsKey(selectedName)) {
            JOptionPane.showMessageDialog(this, "Pilih nama karyawan terlebih dahulu.");
            System.out.println("Karyawan belum dipilih atau tidak ditemukan di map.");
            return;
        }

        int idKaryawan = karyawanMap.get(selectedName);
        System.out.println("ID Karyawan terpilih: " + idKaryawan);

        // Ambil nilai dari combo box (langsung sebagai String, misal "Baik", "Cukup")
        String productivity = (String) productivityComboBox.getSelectedItem();
        String quality = (String) qualityBox.getSelectedItem();
        String tanggungJawab = (String) tanggungJawabBox.getSelectedItem();
        String improvement = (String) improvmentBox.getSelectedItem();
        String absensi = (String) absensiBox.getSelectedItem();
        String psikotes = (String) psikotesBox.getSelectedItem();

        // Log nilai-nilainya
        System.out.println("Nilai input:");
        System.out.println("Productivity: " + productivity);
        System.out.println("Quality: " + quality);
        System.out.println("Tanggung Jawab: " + tanggungJawab);
        System.out.println("Improvement: " + improvement);
        System.out.println("Absensi: " + absensi);
        System.out.println("Psikotes: " + psikotes);

        // Set ke model kandidat
        KandidatModel kandidat = new KandidatModel();
        kandidat.setIdKaryawan(idKaryawan);
        kandidat.setProductivity(productivity);
        kandidat.setQuality(quality);
        kandidat.setTanggungJawab(tanggungJawab);
        kandidat.setImprovment(improvement);
        kandidat.setAbsensi(absensi);
        kandidat.setPsikotes(psikotes);

        // Simpan ke DB
        int result = kandidatDao.create(kandidat);
        if (result > 0) {
            JOptionPane.showMessageDialog(this, "Data kandidat berhasil disimpan.");
            System.out.println("Data kandidat berhasil disimpan ke database.");
            getAllData(); // Refresh tabel
        } else {
            JOptionPane.showMessageDialog(this, "Gagal menyimpan data kandidat.");
            System.out.println("Gagal menyimpan data kandidat.");
        }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            String templateName = "KaryawanTerbaik.jrxml";
            InputStream reportStream = ReportView.class.getResourceAsStream("/resources/reports/" + templateName);

            if (reportStream == null) {
                throw new IllegalArgumentException("File report tidak ditemukan: " + templateName);
            }

            JasperDesign jd = JRXmlLoader.load(reportStream);
            JasperReport jr = JasperCompileManager.compileReport(jd);

            Connection dbConnection = DatabaseUtil.getInstance().getConnection();

            HashMap<String, Object> parameter = new HashMap<>();
            parameter.put("PATH", "src/resources/images/");

            JasperPrint jp = JasperFillManager.fillReport(jr, parameter, dbConnection);
            JasperViewer.viewReport(jp, false);

        } catch (JRException e) {
            System.err.println("Terjadi kesalahan saat memproses laporan JasperReports:");
            e.printStackTrace();
        } catch (Exception e) {
            System.err.println("Terjadi kesalahan:");
            e.printStackTrace();
        }
    }//GEN-LAST:event_jButton1ActionPerformed

