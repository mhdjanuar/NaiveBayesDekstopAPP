     public void getAllData() {
        List<KandidatModel> kandidatList = kandidatDao.findAll();

        DefaultTableModel model = new DefaultTableModel();
        model.setColumnIdentifiers(new Object[]{
            "Nama Karyawan", "Productivity", "Quality", "Tanggung Jawab", 
            "Improvement", "Absensi", "Psikotest", "Hasil"
        });

        for (KandidatModel kandidat : kandidatList) {
            model.addRow(new Object[]{
                kandidat.getNameKaryawan(),
                kandidat.getProductivity(),
                kandidat.getQuality(),
                kandidat.getTanggungJawab(),
                kandidat.getImprovment(),
                kandidat.getAbsensi(),
                kandidat.getPsikotes(),
                kandidat.getHasil()
            });
        }

        jTable1.setModel(model);
    }

    public void getKaryawanComboBox() {
        List<KaryawanModel> karyawanList = karyawanDao.findAll();

        jComboBox1.removeAllItems(); // Kosongkan comboBox dulu
        karyawanMap.clear(); // Bersihkan mapping sebelumnya

        if (karyawanList.isEmpty()) {
            System.out.println("Tidak ada data karyawan ditemukan.");
            return;
        }

        for (KaryawanModel karyawan : karyawanList) {
            String nama = karyawan.getName();
            Integer id = karyawan.getId();

            jComboBox1.addItem(nama); // Tambahkan nama ke comboBox
            karyawanMap.put(nama, id); // Simpan mapping nama -> id

            // Log tiap item yang dimasukkan
            System.out.println("Karyawan dimuat: " + nama + " (ID: " + id + ")");
        }

        System.out.println("Total karyawan dimuat ke comboBox: " + karyawanList.size());
    }

    public void populateComboBox(JComboBox<String> comboBox, List<String> items) {
        comboBox.removeAllItems();
        for (String item : items) {
            comboBox.addItem(item);
        }
    }
